	public static void digEasyClue(Client player) {
		if (!player.getItems().playerHasItem(2692)) {
			return;
		}
		player.startAnimation(830);
		if (player.absX >= 2966 && player.absX <= 2968 && player.absY >= 3413 && player.absY <= 3416)
		{
			giveEasyClueReward(player);
			player.getItems().removeItem(2692, 1);
		}
	}
	
